function myRandom (val) {
    if (g_seconds == 59) {
        return val;
    }
    var _span = val / ((60 - g_seconds) * 1.0);
    var _chunk = _span / 10.0;
    var x = (.5 - Math.random()) * _chunk;
    return Math.max(0, Math.round(x + _span));
}

function isBlink (val) {
    if (val >= 1000) {
        return true;
    }
    else {
        if (Math.random () <= (val / 1000.0)) {
            return true;
        }
        else {
            return false;
        }
    }
}

function getData () {
    //清空人数
    g_dispData = []
    g_blinkData = []
    for (var i = 0; i < g_minData.length; i++) {
        var geoCoord = g_geoCoordMap[g_minData[i].name];
    //    var j = myRandom(g_minData[i].value);
        if (typeof(geoCoord) != "undefined") {
            var j = myRandom(g_minData[i].value);
            g_minData[i].value -= j;
            if (j != 0) {
                g_dispData.push({
                    name:  g_minData[i].name,
                    value: j
                });
            }

            if (isBlink (j) == true) {
                g_blinkData.push({
                    name:  g_minData[i].name,
                    value: j
                });
            }
        }
    }
}

